class Solution {
public:
    bool canJump(vector<int>& nums) {
        int n = nums.size();
        bool dp[n];

        // ğŸ¸ ä»è¿™é‡Œå‡ºå‘ï¼Œæ‰€ä»¥ç¬¬ 0 ä¸ªçŸ³å­å¿…ç„¶ä¸º true
        dp[0] = true;

        for (int i = 1; i < n; i++) {
            dp[i] = false;
            for (int j = 0; j < i; j++) {
                if (nums[j] + j >= i)
                    dp[i] = dp[i] || dp[j];
                if (dp[i]) break;
            }
        }
        return dp[n-1];
    }
};